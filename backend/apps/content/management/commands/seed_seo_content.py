class Command(BaseCommand):
    help = "Seed SEO news articles and case studies (idempotent by slug)."

    def add_arguments(self, parser):
        parser.add_argument("--news-only", action="store_true")
        parser.add_argument("--cases-only", action="store_true")

    def handle(self, *args, **options):
        now = timezone.now()
        if not options["cases_only"]:
            for item in NEWS_ARTICLES:
                NewsPost.objects.update_or_create(
                    slug=item["slug"],
                    defaults={
                        "title": item["title"],
                        "excerpt": item["excerpt"],
                        "body": item["body"],
                        "meta_title": item["title"],
                        "meta_description": item["excerpt"],
                        "is_published": True,
                        "published_at": now,
                    },
                )
            self.stdout.write(f"News: {len(NEWS_ARTICLES)} articles")

        if not options["news_only"]:
            for item in CASE_STUDIES:
                CaseStudy.objects.update_or_create(
                    slug=item["slug"],
                    defaults={
                        "title": item["title"],
                        "excerpt": item["excerpt"],
                        "body": item["body"],
                        "industry": item["industry"],
                        "meta_title": item["title"],
                        "meta_description": item["excerpt"],
                        "is_published": True,
                        "published_at": now,
                    },
                )
            self.stdout.write(f"Cases: {len(CASE_STUDIES)} studies")

        self.stdout.write(self.style.SUCCESS("Done"))
